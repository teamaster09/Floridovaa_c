#include <stdio.h>

int main(void)
{
    // Задание 4: Остаток от деления
    printf("Задание 4: Остаток от деления\n");
    
    int num1, num2;  // Две переменные для чисел
    
    // Просим пользователя ввести числа
    printf("Введите первое число: ");
    scanf("%d", &num1);
    
    printf("Введите второе число: ");
    scanf("%d", &num2);
    
    // Вычисляем остаток от деления num1 на num2 с помощью оператора %
    int remainder = num1 % num2;
    
    // Выводим результат
    // %% нужен, чтобы напечатать один символ % на экране
    printf("%d %% %d = %d\n", num1, num2, remainder);
    
    return 0;
}