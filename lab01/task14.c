#include <stdio.h>

int main(void) {
    int a, b, c;

    printf("Переменная a: адрес = %p, значение = %d\n", (void*)&a, a);
    printf("Переменная b: адрес = %p, значение = %d\n", (void*)&b, b);
    printf("Переменная c: адрес = %p, значение = %d\n", (void*)&c, c);

    // Вычисление разницы между адресами (в байтах)
    // Адреса вычитаются как указатели на int, поэтому разница будет в количестве элементов int,
    // но мы преобразуем к char*, чтобы получить разницу в байтах.
    long diff_ba = (char*)&b - (char*)&a;
    long diff_cb = (char*)&c - (char*)&b;

    printf("\nРазница между адресами b и a (в байтах): %ld\n", diff_ba);
    printf("Разница между адресами c и b (в байтах): %ld\n", diff_cb);

    return 0;
}
