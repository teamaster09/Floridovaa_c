#include <stdio.h>

int main(void)
{
    // Задание 5: Инкремент и декремент
    printf("Задание 5: Инкремент и декремент\n");
    
    int a = 5;
    printf("Начальное значение a = %d\n\n", a);
    
    // Постфиксный инкремент (a++)
    printf("a++ = %d\n", a++);  // Сначала использует (5), потом увеличивает
    printf("После a++: a = %d\n\n", a);  // Теперь a = 6
    
    // Префиксный инкремент (++a)
    a = 5;  // Возвращаем обратно в 5 для чистоты эксперимента
    printf("++a = %d\n", ++a);  // Сначала увеличивает, потом использует (6)
    printf("После ++a: a = %d\n\n", a);  // a = 6
    
    // Постфиксный декремент (a--)
    a = 5;
    printf("a-- = %d\n", a--);  // Сначала использует (5), потом уменьшает
    printf("После a--: a = %d\n\n", a);  // Теперь a = 4
    
    // Префиксный декремент (--a)
    a = 5;
    printf("--a = %d\n", --a);  // Сначала уменьшает, потом использует (4)
    printf("После --a: a = %d\n\n", a);  // a = 4
    
    return 0;
}